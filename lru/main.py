class LRU(object):
    def __init__(self):
        pass

    def put(self):
        print("put function")

    def get(self):
        print("get function")

    def count(self):
        print("count function")

    def peek(self):
        print("peek function")


lru = LRU()
lru.put()
lru.get()
lru.count()
lru.peek()
